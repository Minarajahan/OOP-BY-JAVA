public class Main {
    public static void main(String[] args) {
        String fruit = "Apple";

        switch (fruit) {
            case "Apple":
                System.out.println("It's an Apple.");
                break;
            case "Banana":
                System.out.println("It's a Banana.");
                break;
            default:
                System.out.println("Unknown fruit.");
        }
    }
}
